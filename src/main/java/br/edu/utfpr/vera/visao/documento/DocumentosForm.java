    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

        if (clienteSelecionado == null) {
            JOptionPane.showMessageDialog(null, "Selecione um cliente");
            return;
        }
        NovoDocumentoForm.Callback callbackDocumento = cliente -> {
            if (cliente != null) {
                documentosList.remove(clienteSelecionado);
                documentosList.add(cliente);
            }
        };

        NovoDocumentoForm ncf = new NovoDocumentoForm(clienteSelecionado, callbackDocumento);
        principal.desktopPane.add(ncf);
        ncf.setVisible(true);

    }//GEN-LAST:event_btnEditarActionPerformed

