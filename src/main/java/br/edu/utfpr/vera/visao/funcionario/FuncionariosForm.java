    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (funcionarioSelecionado == null) {
            JOptionPane.showMessageDialog(null, "Selecione um funcionario");
            return;
        }
        new FuncionarioDao().delete(funcionarioSelecionado);
        JOptionPane.showMessageDialog(null, "Funcionario excluído");
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed
        NovoFuncionarioForm.Callback callbackFuncionario = funcionario -> {
            if (funcionario != null) {
                funcionariosList.add(funcionario);                
            }
        };
        NovoFuncionarioForm nff = new NovoFuncionarioForm(callbackFuncionario);
        principal.desktopPane.add(nff);
        nff.setVisible(true);
        nff.setPosicao();
    }//GEN-LAST:event_btnNovoActionPerformed

