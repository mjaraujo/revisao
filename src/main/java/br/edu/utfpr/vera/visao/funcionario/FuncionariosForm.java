    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

        if (funcionarioSelecionado == null) {
            JOptionPane.showMessageDialog(null, "Selecione um funcionario");
            return;
        }
        NovoFuncionarioForm.Callback callbackFuncionario = funcionario -> {
            if (funcionario != null) {
                funcionariosList.remove(funcionarioSelecionado);
                funcionariosList.add(funcionario);
            }
        };

        NovoFuncionarioForm ncf = new NovoFuncionarioForm(funcionarioSelecionado, callbackFuncionario);
        principal.desktopPane.add(ncf);
        ncf.setVisible(true);

    }//GEN-LAST:event_btnEditarActionPerformed

